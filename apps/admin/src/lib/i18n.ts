export function translate(language: ConsoleLanguage, key: string, fallback?: string) {
  return dictionary[language]?.[key] ?? fallback ?? key;
}

export function translateUiText(language: ConsoleLanguage, text: string) {
  if (!text || language === 'en') return text;
  const direct = uiPhraseDictionary[language]?.[text];
  if (direct) return direct;
  const trimmed = text.trim();
  const normalized = trimmed.replace(/_/g, ' ');
  const byNormalized = uiPhraseDictionary[language]?.[normalized];
  if (byNormalized) return byNormalized;
  const lower = normalized.toLowerCase();
  const lowerEntry = Object.entries(uiPhraseDictionary[language] ?? {}).find(([key]) => key.toLowerCase() === lower);
  return lowerEntry?.[1] ?? text;
}

export function resolveConsoleLanguage(value: string | null | undefined): ConsoleLanguage {
  return value === 'am' ? 'am' : 'en';
}

export function hasExplicitConsoleLanguageChoice() {
  if (typeof window === 'undefined') {
    return false;
  }
  return window.localStorage.getItem(consoleLanguageExplicitKey) === 'true';
}

export function readConsoleLanguage(): ConsoleLanguage {
  if (typeof window === 'undefined') {
    return 'en';
  }
  return resolveConsoleLanguage(window.localStorage.getItem(consoleLanguageStorageKey));
}

export function resolveActiveConsoleLanguage(serverPreference?: string | null | undefined): ConsoleLanguage {
  if (typeof window === 'undefined') {
    return resolveConsoleLanguage(serverPreference);
  }

  if (hasExplicitConsoleLanguageChoice()) {
    return readConsoleLanguage();
  }

  return resolveConsoleLanguage(serverPreference);
}

export function persistConsoleLanguage(language: ConsoleLanguage, options?: { explicit?: boolean }) {
  if (typeof window === 'undefined') {
    return;
  }

  window.localStorage.setItem(consoleLanguageStorageKey, language);
  if (options?.explicit) {
    window.localStorage.setItem(consoleLanguageExplicitKey, 'true');
  }
  window.dispatchEvent(new Event('tikur-abay-language-change'));
}
